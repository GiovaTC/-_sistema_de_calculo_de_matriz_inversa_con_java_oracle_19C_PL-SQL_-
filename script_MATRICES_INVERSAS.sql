-- ? Tabla: MATRICES_INVERSAS
CREATE TABLE MATRICES_INVERSAS (
    ID              NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TAMANIO         NUMBER,
    MATRIZ_ORIGINAL CLOB,
    MATRIZ_INVERSA  CLOB,
    FECHA_REGISTRO  DATE DEFAULT SYSDATE
);

COMMIT;

-- ?? Procedimiento Almacenado: SP_GUARDAR_MATRIZ_INVERSA
CREATE OR REPLACE PROCEDURE SP_GUARDAR_MATRIZ_INVERSA (
    P_TAMANIO         IN NUMBER,
    P_MATRIZ_ORIGINAL IN CLOB,
    P_MATRIZ_INVERSA  IN CLOB
)
IS
BEGIN
    INSERT INTO MATRICES_INVERSAS (
        TAMANIO,
        MATRIZ_ORIGINAL,
        MATRIZ_INVERSA
    ) VALUES (
        P_TAMANIO,
        P_MATRIZ_ORIGINAL,
        P_MATRIZ_INVERSA
    );

    COMMIT;
END;
